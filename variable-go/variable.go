//variable go-lang

package main

import "fmt"

func main() {
	var name string

	name = "Jhon Dhoe"
	fmt.Println(name)

	name = "siti"
	fmt.Println(name)

	var nameFriend = "eko"
	fmt.Println(nameFriend)

	var age = 90
	fmt.Println(age)

	country := "Afrika" //deklarasi variable yang hanya bisa digunakan diawal saja atau sekali saja
	fmt.Println(country)

	var (
		firstName = "Jhon"
		lastName  = "Dhoe"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}
